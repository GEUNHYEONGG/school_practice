# 복합 대입 연산자 (Compound Assignment Operators)
# 연산과 대입을 한 번에 처리한다
#
# 왜 사용하는가?
#   score = score + 10  →  score += 10
#   같은 변수에 연산 결과를 다시 저장할 때 코드를 짧고 명확하게 쓸 수 있다
#
# 언제 사용하는가?
#   - 점수, 금액, 수량 등을 누적할 때
#   - 반복문에서 값을 갱신할 때

# ----------------------------------------
# [1] 복합 대입 연산자 종류
# ----------------------------------------

x = 10

x += 5     # x = x + 5    → 더한 뒤 저장
print("+=", x)   # 15

x -= 3     # x = x - 3    → 뺀 뒤 저장
print("-=", x)   # 12

x *= 2     # x = x * 2    → 곱한 뒤 저장
print("*=", x)   # 24

x /= 4     # x = x / 4    → 나눈 뒤 저장 (결과는 float)
print("/=", x)   # 6.0

x = 25     # x를 다시 25로 설정
x //= 3    # x = x // 3   → 몫을 구한 뒤 저장
print("//=", x)  # 8

x %= 3     # x = x % 3    → 나머지를 구한 뒤 저장
print("%=", x)   # 2

x **= 4    # x = x ** 4   → 거듭제곱한 뒤 저장
print("**=", x)  # 16

# ----------------------------------------
# [2] 실전 예시: 점수 누적
# ----------------------------------------

total = 0
total += 85    # 1번 학생 점수 추가
total += 92    # 2번 학생 점수 추가
total += 78    # 3번 학생 점수 추가
print("총점:", total)  # 255

# ----------------------------------------
# [3] 문자열에도 사용 가능
# ----------------------------------------

message = "주문: "
message += "아메리카노 "
message += "카페라떼 "
message += "녹차"
print(message)  # 주문: 아메리카노 카페라떼 녹차
